## This program accepts Temperature in Celsios from user
##and print out converted value of temperature in Fahrengrit

temperatureC = float(input("Please enter temperature value in degree Celsius: "))

# temperatureC is a value of temperature in Celsius
# temperatureF is a value of temperature in Fahrenheit

# formula to convert temperature from celsius to Fahrenheit is:
# temperatureF = temperatureC * 9/5 + 32

temperatureF = temperatureC * 9/5 + 32      


print("Temprature in Fahrenheit is : ", temperatureF)


